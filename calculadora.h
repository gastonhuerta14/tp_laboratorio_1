/** \brief funcion que suma dos numeros solicitados al usuario
 *
 * \param
 * \return devuelve un numero que es el resultado de la suma
 *
 */
float sumar(float numeroUno,float numeroDos);

/** \brief funcion que resta dos numeros solicitados al usuario
 *
 * \param
 * \return  devuelve un numero que es el resultado de la resta
 *
 */
float restar(float numeroUno,float numeroDos);

/** \brief funcion que multiplica dos numeros solicitados al usuario
 *
 * \param
 * \return devuelve un numero que es el producto de las dos numeros
 *
 */
float multiplicar(float numeroUno,float numeroDos);

/** \brief funcion que divide dos numeros solicitados al usuario
 *
 * \param
 * \return devuelve un numero que es la division de ambos numeros
 *
 */
float division(float numeroUno,float numeroDos);

/** \brief funcion que calcula el factorial de un numero solicitado al usuario
 *
 * \param
 * \return devuelve dos factoriales de cada uno de los numeros pedidos
 *
 */
float factorial(float numero);
